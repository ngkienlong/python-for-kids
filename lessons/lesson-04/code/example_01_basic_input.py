# example_01_basic_input.py
# Lesson 4: Basic input() usage
# In Scratch: "ask and wait" block → in Python: input()

# Ask the user for their name
name = input("What is your name? ")

# Print a greeting using the name
print("Hello,", name)
print("Nice to meet you!")

# input() always PAUSES the program and waits for the user to press Enter.
# Whatever the user types is stored in the variable.

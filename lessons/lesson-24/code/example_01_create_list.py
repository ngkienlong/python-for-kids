# Lesson 24: Creating lists in Python
# In Scratch, you use a List block. In Python, use square brackets []

# A list of fruit names (strings)
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# A list of numbers
scores = [95, 87, 72, 100, 65]
print("Scores:", scores)

# A list of mixed types
mixed = ["Alice", 10, 3.14, True]
print("Mixed:", mixed)

# An empty list (we can add items later)
empty = []
print("Empty list:", empty)
print("Length of empty list:", len(empty))

# Lists can be as long as you want
days = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"]
print("Days of the week:", days)
print("Number of days:", len(days))

# example_01_define_call.py
# Lesson 28: Define and call a function
# In Scratch: "My Blocks" → In Python: def

# Step 1: DEFINE the function (create it)
def greet():
    print("Hello!")
    print("Welcome to Python!")
    print("Let's code!")

# Step 2: CALL the function (run it)
greet()

# The function runs only when you call it
# Nothing happens when you just define it

print()
print("--- Calling greet() again ---")
print()

# Call it again — same result!
greet()

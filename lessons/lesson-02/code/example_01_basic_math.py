# Lesson 2 - Example 1: Basic math operators
# Python can do math just like a calculator!

print(7 + 3)   # Addition: 10
print(7 - 3)   # Subtraction: 4
print(7 * 3)   # Multiplication: 21
print(7 / 2)   # Division: 3.5
print(7 // 2)  # Floor division: 3 (whole number only)
print(7 % 2)   # Modulo: 1 (remainder)

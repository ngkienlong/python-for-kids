# Lesson 2 - Example 5: Integers and Floats
# int = whole number, float = decimal number

print(5)        # int
print(3.5)      # float

# Mixing int and float gives float
print(5 + 2)    # 7 (int + int = int)
print(5 + 2.0)  # 7.0 (int + float = float)

# / always gives float
print(10 / 2)   # 5.0 (float!)

# // gives int when both are int
print(10 // 2)  # 5 (int)

# Lesson 2 - Example 6: Printing text with math
# Use commas to print text and numbers together.

print("5 + 3 =", 5 + 3)
print("10 * 4 =", 10 * 4)
print("100 / 3 =", 100 / 3)

print()

# Negative numbers
print(-5 + 3)       # -2
print(3 - 10)       # -7
print(-3 * -2)      # 6 (negative * negative = positive)

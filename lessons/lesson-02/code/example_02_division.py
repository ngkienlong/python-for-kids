# Lesson 2 - Example 2: Two kinds of division
# / gives decimal answer, // gives whole number only

print("Regular division /")
print(10 / 3)    # 3.3333333333333335
print(7 / 2)     # 3.5
print(10 / 2)    # 5.0 (still a float!)

print()

print("Floor division //")
print(10 // 3)   # 3
print(7 // 2)    # 3
print(10 // 2)   # 5 (integer)

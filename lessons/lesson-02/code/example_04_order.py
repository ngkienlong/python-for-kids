# Lesson 2 - Example 4: Order of operations
# Python follows math rules: *, / before +, -
# Use parentheses () to change the order.

print(2 + 3 * 4)       # 14 (multiplication first)
print((2 + 3) * 4)     # 20 (parentheses first)

print(10 - 6 / 2)      # 7.0 (division first)
print((10 - 6) / 2)    # 2.0 (parentheses first)

# Tip: use parentheses to make your code clear!
print((10 + 20) * 3)   # 90

# Lesson 2 - Example 3: Modulo (remainder)
# The % operator gives the remainder after division.

print(10 % 3)   # 1  (10 = 3 * 3 + 1)
print(15 % 4)   # 3  (15 = 4 * 3 + 3)
print(20 % 5)   # 0  (20 = 5 * 4 + 0, no remainder)

print()

# Check even or odd
print("Is 8 even?", 8 % 2)    # 0 means yes (even)
print("Is 7 even?", 7 % 2)    # 1 means no (odd)

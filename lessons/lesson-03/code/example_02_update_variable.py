# Lesson 3 - Example 2: Updating a variable
# You can change the value stored in a variable.

score = 0
print("Start:", score)    # 0

score = 10
print("After set:", score)  # 10

score = score + 5   # take old value, add 5, store back
print("After +5:", score)   # 15

score = score - 3
print("After -3:", score)   # 12

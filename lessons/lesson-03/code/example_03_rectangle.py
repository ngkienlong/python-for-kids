# Lesson 3 - Example 3: Variables make code cleaner
# Use variables for rectangle calculations.

width = 15
height = 8

# Calculate using variables
area = width * height
perimeter = 2 * width + 2 * height

print("Width:", width)
print("Height:", height)
print("Area:", area)
print("Perimeter:", perimeter)

# If you change width or height, everything updates automatically!
